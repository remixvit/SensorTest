void LCD_Clear(void)
{
  int ix;
  for(ix=0;ix<1024;ix++){lcd_buff[ix]=0x00;}
  x_cur=0;
  y_cur=0;
}

void LCD_Update(void)
{
    volatile uint32 Error = 0;
    volatile uint16 Index;
    for(Index = 0; Index < 1024; Index++)
    {
        Error = I2C_SendCommand(SSD1306_CONTROL_DATA, lcd_buff[Index]);
    }
}

void LCD_Chr(uint8_t ch)
{
    uint8_t i;
    
    lcd_buff_idx=y_cur*128+x_cur*6;    
    if ((ch>=0x20)&&(ch<=0x80)) ch-=32;
    else if (ch>=0xC0) ch-=96;
    else ch = 95;
    
    for (i=0;i<5;i++)
        {
            lcd_buff[lcd_buff_idx++] = font[ch][i];
        }
    lcd_buff[lcd_buff_idx++] = 0x00;
    
    x_cur++;
    if (x_cur>20)
    {
      x_cur=0;
      y_cur++;
      if (y_cur>7)
      {
        y_cur=0;
      }
    }
}

void LCD_2xChr(uint8_t ch)
{
    uint8_t i, c;
    uint8_t b1, b2;  
    uint16_t  tmpIdx;

    lcd_buff_idx=y_cur*128+x_cur*6;
    tmpIdx=lcd_buff_idx-128;
    if ((ch>=0x20)&&(ch<=0x7F)) ch-=32;
    else if (ch>=0xC0) ch-=96;
    else ch = 95;
    
    for ( i = 0; i < 5; i++ )
      {
        c = font[ch][i]<<1;
        b1=(c&0x01)*3;
        b1|=(c&0x02)*6;
        b1|=(c&0x04)*12;
        b1|=(c&0x08)*24;

        c>>=4;
        b2=(c&0x01)*3;
        b2|=(c&0x02)*6;
        b2|=(c&0x04)*12;
        b2|=(c&0x08)*24;

        lcd_buff[tmpIdx++]=b1;
        lcd_buff[tmpIdx++]=b1;
        lcd_buff[tmpIdx+126]=b2;
        lcd_buff[tmpIdx+127]=b2;
      }
    lcd_buff_idx=(lcd_buff_idx+11)%1024;
    lcd_buff[lcd_buff_idx]=0x00;    
    if(lcd_buff_idx==1023) lcd_buff_idx=0;
    else lcd_buff_idx++;
    x_cur=x_cur+2;
    if (x_cur>19)
    {
      x_cur=0;
      y_cur=y_cur+2;
      if (y_cur>7)
      {
        y_cur=0;
      }
    }
}

void LCD_FStr(char *dataPtr)
{
    uint8_t ix=0;
    while(dataPtr[ix]!=0)
    {
        LCD_Chr(dataPtr[ix]);
        ix++;
    }
}

void LCD_2xFStr(char *dataPtr)
{
    uint8_t ix=0;
    while(dataPtr[ix]!=0)
    {
        LCD_2xChr(dataPtr[ix]);
        ix++;
    }
}

void LCD_GotoXY(uint8_t x,uint8_t y)
{
    if((x<21)&&(y<8))
    {
      x_cur=x;
      y_cur=y;
    }
}

void LCD_Picture(unsigned char const *data)
{
    for (lcd_buff_idx=0;lcd_buff_idx<1024;lcd_buff_idx++)
    {
        lcd_buff[lcd_buff_idx] = data[lcd_buff_idx];
    } 
}

