uint32 I2C_SendCommand(uint8 Controll, uint8 Data)
{
    volatile uint32 Error;
    uint8 buf[2] = {Controll, Data};
    Error = I2C_SendBuffer(SSD1306_I2C_ADRESS, buf, 2);
    return Error;
}

